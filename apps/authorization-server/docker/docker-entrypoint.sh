#!/bin/bash

function checkEnv() {
  if [[ -z "$SESSION_SECRET" ]]; then
    echo "SESSION_SECRET is not set"
    exit 1
  fi
  if [[ -z "$COOKIE_MAX_AGE" ]]; then
    echo "COOKIE_MAX_AGE is not set"
    exit 1
  fi
  if [[ -z "$SESSION_NAME" ]]; then
    echo "SESSION_NAME is not set"
    exit 1
  fi
  if [[ -z "$TOKEN_VALIDITY" ]]; then
    echo "TOKEN_VALIDITY is not set"
    exit 1
  fi
  if [[ -z "$DB_HOST" ]]; then
    echo "DB_HOST is not set"
    exit 1
  fi
  if [[ -z "$DB_NAME" ]]; then
    echo "DB_NAME is not set"
    exit 1
  fi
  if [[ -z "$DB_USER" ]]; then
    echo "DB_USER is not set"
    exit 1
  fi
  if [[ -z "$DB_PASSWORD" ]]; then
    echo "DB_PASSWORD is not set"
    exit 1
  fi
  if [[ -z "$NODE_ENV" ]]; then
    echo "NODE_ENV is not set"
    exit 1
  fi
  if [[ -z "$REDIS_PROTO" ]]; then
    export REDIS_PROTO=redis
  fi
}

function checkConnection() {
  # Wait for services
  su craft -c "node ./docker/check-db.js"
  if [[ ! -z "$REDIS_HOST" ]] && [[ ! -z "$REDIS_PORT" ]]; then
    echo "Connect Redis . . ."
    timeout 10 bash -c 'until printf "" 2>>/dev/null >>/dev/tcp/$0/$1; do sleep 1; done' $REDIS_HOST $REDIS_PORT
  fi
}

function configureServer() {
  if [ ! -f .env ]; then
    envsubst '${NODE_ENV}
      ${SESSION_SECRET}
      ${COOKIE_MAX_AGE}
      ${SESSION_NAME}
      ${TOKEN_VALIDITY}
      ${DB_HOST}
      ${DB_NAME}
      ${DB_USER}
      ${DB_PASSWORD}' \
      < docker/env.tmpl > .env

    if [[ ! -z "$REDIS_PROTO" ]] &&
      [[ ! -z "$REDIS_HOST" ]] &&
      [[ ! -z "$REDIS_PORT" ]] &&
      [[ ! -z "$REDIS_PASSWORD" ]]; then
      envsubst '${REDIS_PROTO}
        ${REDIS_HOST}
        ${REDIS_PORT}
        ${REDIS_PASSWORD}'\
        < docker/env-redis.tmpl >> .env
    fi
  fi
}

export -f configureServer

if [ "$1" = 'start' ]; then
  # Validate environment variables
  checkEnv
  # Validate DB Connection
  checkConnection
  # Configure server
  su craft -c "bash -c configureServer"
  su craft -c "node dist/out-tsc/main.js"
fi

exec runuser -u craft "$@"
